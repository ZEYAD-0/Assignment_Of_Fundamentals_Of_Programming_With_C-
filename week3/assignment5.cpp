#include<iostream>

int main(void)
{
  char a = '~', b = '&', c = '%', d = 'A';
  
  std::cout << "ASCII Value of ~ Is " << int(a) << "\n";
  std::cout << "ASCII Value of & Is " << int(b) << "\n";
  std::cout << "ASCII Value of % Is " << int(c) << "\n";
  std::cout << "ASCII Value of A Is " << int(d) << "\n";

  return 0;
}